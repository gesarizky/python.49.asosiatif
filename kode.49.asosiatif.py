# Menginput
print('Isi nilai-nilai Dengan Benar ')
a = int(input("Masukan Nilai A  : "))
b = int(input("Masukan Nilai B  : "))
c = int(input("Masukan Nilai C  : "))

# Mengkonversi
hasilplus = a + b + c
hasilkali = a * b * c

# Menampilkan Hasil
print('================================================================================')
print('Jika Penjumlahan (' ,a, ' + ' ,b, ')+ ' ,c, ' = ' ,a, ' + (' ,b, ' + ' ,c, ') = d ')
print('Maka Maka Hasilnya = ',hasilplus)
print('================================================================================')
print('Jika Perkalian (' ,a, ' x ' ,b, ')x ' ,c, ' = ' ,a, ' x (' ,b, ' x ' ,c, ') = d ')
print('Maka Maka Hasilnya = ',hasilkali)
print('================================================================================')
